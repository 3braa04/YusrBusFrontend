import { useAuth } from "@/app/core/auth/authContext";
import { useLoggedInUser } from "@/app/core/contexts/loggedInUserContext";
import { useSetting } from "@/app/core/contexts/settingContext";
import { LoginRequest } from "@/app/core/data/loginRequest";
import { useFormValidation, type ValidationRule } from "@/app/core/hooks/useFormValidation";
import ApiConstants from "@/app/core/networking/apiConstants";
import SettingsApiService from "@/app/core/networking/services/settingsApiService";
import YusrApiHelper from "@/app/core/networking/yusrApiHelper";
import { Validators } from "@/app/core/utils/validators";
import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";
import {
  Field,
  FieldDescription,
  FieldGroup,
  FieldLabel,
} from "@/components/ui/field";
import { Input } from "@/components/ui/input";
import { cn } from "@/lib/utils";
import { Loader2 } from "lucide-react";
import { useState } from "react";
import { useLocation, useNavigate } from "react-router-dom";
import type User from "../users/data/user";

import placeholderImg from "@/assets/placeholder.svg";

export function LoginForm({
  className,
  ...props
}: React.ComponentProps<"div">) {

  const navigate = useNavigate();
  const [formData, setFormData] = useState<Partial<LoginRequest>>({});
  const [apiError, setApiError] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const { login } = useAuth();
  const location = useLocation();
  const { updateSetting } = useSetting();
  const { updateLoggedInUser } = useLoggedInUser();

  const validationRules: ValidationRule<Partial<LoginRequest>>[] = [
    {
      field: "email",
      selector: (d) => d.companyEmail,
      validators: [Validators.required("البريد الإلكتروني مطلوب")],
    },
    {
      field: "username",
      selector: (d) => d.username,
      validators: [Validators.required("اسم المستخدم مطلوب")],
    },
    {
      field: "password",
      selector: (d) => d.password,
      validators: [Validators.required("كلمة المرور مطلوبة")],
    },
  ];
  const { getError, isInvalid, validate, clearError, errorInputClass } =
    useFormValidation(formData, validationRules);

  const Login = async () => {

    if(!validate())
      return;

    const request = new LoginRequest({
      companyEmail: formData.companyEmail,
      username: formData.username,
      password: formData.password,
    });

    setLoading(true);

    const result = await YusrApiHelper.Post<User>(
      `${ApiConstants.baseUrl}/Login`,
      request,
    );

    if (result.status === 200) {
      login();

      const settingPromise = new SettingsApiService().Filter();
      settingPromise.then(setting => {
        if (setting.data) updateSetting(setting.data);
      });

      if (result.data) {
        updateLoggedInUser(result.data);
      }

      const origin = location.state?.from?.pathname || "/dashboard";
      
      setLoading(false);

      setTimeout(() => {
        navigate(origin, { replace: true });
      }, 10);
    }
  };

  return (
    <div className={cn("flex flex-col gap-6", className)} {...props}>
      <Card className="overflow-hidden p-0">
        <CardContent className="grid p-0 md:grid-cols-2">
          <form className="p-6 md:p-8">
            <FieldGroup>
              <div className="flex flex-col items-center gap-2 text-center">
                <h1 className="text-2xl font-bold">أهلا بك مجددًا</h1>
                <p className="text-muted-foreground text-balance">
                  سجل الدخول إلى حسابك
                </p>
              </div>
              <Field>
                <FieldLabel htmlFor="email">البريد الإلكتروني</FieldLabel>
                <Input
                  id="email"
                  type="email"
                  placeholder="m@example.com"
                  value={formData.companyEmail || ""}
                  onChange={(e) => {
                    setFormData({ ...formData, companyEmail: e.target.value });
                    clearError("email");
                    setApiError(null);
                  }}
                  className={errorInputClass("email")}
                  required
                />
                {isInvalid("email") && (
                  <span className="text-xs text-red-500">{getError("email")}</span>
                )}
              </Field>
              <Field>
                <div className="flex items-center">
                  <FieldLabel htmlFor="username">اسم المستخدم</FieldLabel>
                </div>
                <Input
                  id="username"
                  type="text"
                  value={formData.username || ""}
                  onChange={(e) => {
                    setFormData({ ...formData, username: e.target.value });
                    clearError("username");
                    setApiError(null);
                  }}
                  className={errorInputClass("username")}
                  required
                />
                {isInvalid("username") && (
                  <span className="text-xs text-red-500">{getError("username")}</span>
                )}
              </Field>
              <Field>
                <div className="flex items-center">
                  <FieldLabel htmlFor="password">كلمة المرور</FieldLabel>
                </div>
                <Input
                  id="password"
                  type="password"
                  value={formData.password || ""}
                  onChange={(e) => {
                    setFormData({ ...formData, password: e.target.value });
                    clearError("password");
                    setApiError(null);
                  }}
                  className={errorInputClass("password")}
                  required
                />
                {isInvalid("password") && (
                  <span className="text-xs text-red-500">{getError("password")}</span>
                )}
              </Field>

              {apiError && (
                <div className="text-sm font-medium text-red-600">
                  {apiError}
                </div>
              )}

              <Field>
                <Button type="button" disabled={loading} onClick={Login}>
                  {loading && <Loader2 className="ml-2 h-4 w-4 animate-spin" />}
                  تسجيل الدخول
                </Button>
              </Field>
              <FieldDescription className="text-center">
                لا تملك حسابًا بعد؟ <a href="#">سجل معنا</a>
              </FieldDescription>
            </FieldGroup>
          </form>
          <div className="bg-muted relative hidden md:block">
            <img
              src={placeholderImg}
              alt="Image"
              className="absolute inset-0 h-full w-full object-cover dark:brightness-[0.2] dark:grayscale"
            />
          </div>
        </CardContent>
      </Card>
    </div>
  );
}
